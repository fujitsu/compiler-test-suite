int 
scqih02 (void)
{
    int k, x[8], col[8], up[15], down[15];

    n = 0;
    for( k = 0; k < 8; k++)
        col[k] = TRUE;
    for( k = 0; k < 15; k++)
        up[k] = down[k] = TRUE;
    generate( x, col, up, down);
    if ( errflg == FALSE)
        printf("SCQIH02  PASS.\n");
    else
        printf("SCQIH02  NG.\n");
}


void 
generate (int x[8], int col[8], int up[15], int down[15])
{
    int h, k;

    for( h = 0; h < 8; h++){
        if ((col[h] == TRUE) &&
            (up[n-h+7] == TRUE) &&
            (down[n+h] == TRUE) ){
            x[n] = h;
            col[h] = up[n-h+7] = down[n+h] = FALSE;
            n++;
            if ( n == 8){
                for (k = 0; k < 8; k++){
                    if ( res[colm][k] != x[k]){
                        printf("ERROR at RES[%3d][%3d]\n",
                               colm, k);
                        errflg = TRUE;
                    }
                }
                colm ++;
            }else
                generate( x, col, up, down);
            n--;
            col[h] = up[n-h+7] = down[n+h] = TRUE;
        }
    }
}

