  double	Rzas, Rzbs, Rzcs, Rzds, Rzes;
  double	sas, sbs, scs, sds, ses;
  double	sx2s, sy2s, sz2s;
  double	Rsas, Rsbs, Rscs, Rsds, Rses;
  double	xax, xbx, xcx, xdx, xex;
  double	xay, xby, xcy, xdy, xey;
  double	xaz, xbz, xcz, xdz, xez;
