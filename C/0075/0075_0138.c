    ybs = cxs + ABz*yxs;
    ycs = cys + ABz*yys;
    yds = 0.5*( axs + ABx*yxs - y2ys - ABy*yys );
    yes = SQR3I*( czs + ABz*yzs			       -0.5*( axs + ABx*yxs + y2ys + ABy*yys ) );
    zas = bys + ABx*zys;
    zbs = bzs + ABx*zzs;
    zcs = czs + ABy*zzs;
    zds = 0.5*( bxs + ABx*zxs - cys - ABy*zys );
    zes = SQR3I*( z2zs + ABz*zzs			       -0.5*( bxs + ABx*zxs + cys + ABy*zys ) );

