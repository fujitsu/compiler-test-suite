    bes = SQR3I*( bzzsw - 0.5*( bxxsw + byysw ) );
    cas = Gbx*cys;
    cbs = Gbx*czs;
    ccs = Gby*czs + Rzzs;
    cds = 0.5*( cxxsw - cyysw );
    ces = SQR3I*( czzsw - 0.5*( cxxsw + cyysw ) );
    das = Gbx*dys + Rxys;
    dbs = Gbx*dzs + Rxzs;
    dcs = Gby*dzs - Ryzs;
    dds = 0.5*( dxxsw - dyysw );
    des = SQR3I*( dzzsw - 0.5*( dxxsw + dyysw ) );
    eas = Gbx*eys - SQR3I*Rxys;
    ebs = Gbx*ezs - SQR3I*Rxzs;
    ecs = Gby*ezs - SQR3I*Ryzs;
    eds = 0.5*( exxsw - eyysw );
    ees = SQR3I*( ezzsw - 0.5*( exxsw + eyysw ) );

    Raxs = RZGP5*axs;
    Rays = RZGP5*ays;
    Razs = RZGP5*azs;
    Rbxs = RZGP5*bxs;
