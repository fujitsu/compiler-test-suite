    Rdxs = RZGP5*dxs;
    Rdys = RZGP5*dys;
    Rdzs = RZGP5*dzs;
    Rexs = RZGP5*exs;
    Reys = RZGP5*eys;
    Rezs = RZGP5*ezs;
    Rxas = RZGP5*xas;
    Rxbs = RZGP5*xbs;
    Rxcs = RZGP5*xcs;
    Rxds = RZGP5*xds;
    Rxes = RZGP5*xes;
    Ryas = RZGP5*yas;
    Rybs = RZGP5*ybs;
    Rycs = RZGP5*ycs;
    Ryds = RZGP5*yds;
    Ryes = RZGP5*yes;
    Rzas = RZGP5*zas;
    Rzbs = RZGP5*zbs;
    Rzcs = RZGP5*zcs;
    Rzds = RZGP5*zds;
    Rzes = RZGP5*zes;

    aax = Gcx*aas + Ryas + Rays;
    aay = Gcy*aas + Rxas + Raxs;
    aaz = Gcz*aas;
    abx = Gcx*abs + Rybs + Razs;
    aby = Gcy*abs + Rxbs;
    abz = Gcz*abs        + Raxs;
    acx = Gcx*acs + Rycs;
    acy = Gcy*acs + Rxcs + Razs;
    acz = Gcz*acs        + Rays;
    adx = Gcx*ads + Ryds + Raxs;
    ady = Gcy*ads + Rxds - Rays;
    adz = Gcz*ads;
    aex = Gcx*aes + Ryes - SQR3I *Raxs;
    aey = Gcy*aes + Rxes - SQR3I *Rays;
    aez = Gcz*aes        + SQR3I2*Razs;

    bax = Gcx*bas + Rzas + Rbys;
    bay = Gcy*bas        + Rbxs;
    baz = Gcz*bas + Rxas;
    bbx = Gcx*bbs + Rzbs + Rbzs;
    bby = Gcy*bbs;
    bbz = Gcz*bbs + Rxbs + Rbxs;
    bcx = Gcx*bcs + Rzcs;
    bcy = Gcy*bcs        + Rbzs;
    bcz = Gcz*bcs + Rxcs + Rbys;
    bdx = Gcx*bds + Rzds + Rbxs;
    bdy = Gcy*bds        - Rbys;
    bdz = Gcz*bds + Rxds;
    bex = Gcx*bes + Rzes - SQR3I *Rbxs;
    bey = Gcy*bes        - SQR3I *Rbys;
    bez = Gcz*bes + Rxes + SQR3I2*Rbzs;

    cax = Gcx*cas        + Rcys;
    cay = Gcy*cas + Rzas + Rcxs;
    caz = Gcz*cas + Ryas;
    cbx = Gcx*cbs        + Rczs;
    cby = Gcy*cbs + Rzbs;
    cbz = Gcz*cbs + Rybs + Rcxs;
    ccx = Gcx*ccs;
    ccy = Gcy*ccs + Rzcs + Rczs;
    ccz = Gcz*ccs + Rycs + Rcys;
    cdx = Gcx*cds        + Rcxs;
    cdy = Gcy*cds + Rzds - Rcys;
    cdz = Gcz*cds + Ryds;
    cex = Gcx*ces        - SQR3I *Rcxs;
    cey = Gcy*ces + Rzes - SQR3I *Rcys;
