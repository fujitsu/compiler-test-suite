      SUBROUTINE FFT

      IMPLICIT REAL*8 (A-H,O-Z)
      COMMON/B1/NR,NP,NZ,PR,DT2
      COMMON/B3/PI,R(17),DR(17),P(0:97),DP,DZ
      COMMON/B4/RR(17),DRR(17),RDP(17),RRDP(17),RDRR(17),RRDR(17)
      COMMON/B7/CLRR(17),CLRR0(17),CLRR1(17),CLRR2(17)
      COMMON/B10/COSP(0:97),SINP(0:97),SID
      COMMON/B11/XX1(0:97,17,0:33),XX2(0:97,17,0:33),U(0:97,17,0:33)
      COMMON/B12/HA(0:97,0:97),HB(0:33,0:33)
      COMMON/B14/VRN(0:97,17,0:33),VPN(0:97,17,0:33),VZN(0:97,17,0:33)
      COMMON/B15/VR(0:97,17,0:33),VP(0:97,0:17,0:33),VZ(0:97,0:17,0:33)
      COMMON/B21/TR(0:97,17,0:33),TP(0:97,17,0:33),TZ(0:97,17,0:33)
      COMMON/B22/SP(0:97,0:17,0:33)
      INTEGER AL,BE
      REAL*8 D(0:97,17,0:33),DA(0:97,17,0:33),DAB(0:97,17,0:33),
     &     SPA(0:97,17,0:33),SPAB(0:97,17,0:33),V(0:97,17,0:33),
     &     BC1(0:97,0:33),BC2(0:97,0:33)

      DO 40 K=1,NZ
      DO 40 J=1,NP
         D(J,1,K)=D(J,1,K)+CLRR1(2)
   40 CONTINUE

      DO 100 I=1,NR-1
      DO 100 K=1,NZ
      DO 100 AL=0,NP-1
       S1=D(1,I,K)*HA(AL,1)+D(2,I,K)*HA(AL,2)+D(3,I,K)*HA(AL,3)
       DA(AL,I,K)=S1+S2
  100 CONTINUE

      RETURN
      END
