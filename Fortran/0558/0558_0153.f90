MODULE mod1
IMPLICIT NONE

INTEGER :: num

CONTAINS
SUBROUTINE msub(dd1)
  INTEGER :: dd1
  dd1 = dd1 * 2
END SUBROUTINE

END MODULE


PROGRAM main
USE mod1
IMPLICIT NONE

INTERFACE gnr
  MODULE PROCEDURE msub
END INTERFACE

num = 14
CALL gnr(num)

IF(num .EQ. 28) THEN
  PRINT*,"PASS"
ELSE
  PRINT*,"ERROR"
END IF

END PROGRAM
