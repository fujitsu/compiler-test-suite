MODULE mod1
IMPLICIT NONE

INTERFACE gnr
  MODULE PROCEDURE msub
END INTERFACE

PRIVATE :: msub

CONTAINS
SUBROUTINE msub(dd1)
  INTEGER :: dd1
  dd1 = 2
END SUBROUTINE

END MODULE


PROGRAM main
USE mod1
IMPLICIT NONE

INTEGER :: rr

CALL gnr(rr)

IF(rr .EQ. 2) THEN
  PRINT*,"PASS"
ELSE
  PRINT*,"ERROR"
END IF

END PROGRAM
