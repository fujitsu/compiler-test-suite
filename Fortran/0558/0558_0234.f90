MODULE mod1
IMPLICIT NONE

TYPE ty
  INTEGER :: num 
END TYPE

INTERFACE
SUBROUTINE msub(dd1,dd2)
  IMPORT ty
  CLASS(ty) :: dd1,dd2
END SUBROUTINE
SUBROUTINE psub(dd1)
  IMPORT ty
  CLASS(ty) :: dd1
END SUBROUTINE
END INTERFACE

END MODULE


PROGRAM main
USE mod1
IMPLICIT NONE

CLASS(ty),ALLOCATABLE :: obj
ALLOCATE(ty :: obj)

obj%num = 14
CALL int_sub()

IF(obj%num .EQ. 28) THEN
  PRINT*,"PASS"
ELSE
  PRINT*,"ERROR"
END IF

CONTAINS
SUBROUTINE int_sub()
PROCEDURE(psub) :: prc
INTERFACE gnr
  PROCEDURE prc
END INTERFACE
CALL gnr(obj)
END SUBROUTINE

END PROGRAM

SUBROUTINE msub(dd1,dd2)
  USE mod1
  CLASS(ty) :: dd1,dd2
  SELECT TYPE(dd1)
    TYPE IS(ty)
      dd1%num = dd1%num + dd2%num
  END SELECT
  ENTRY prc(dd1)
  SELECT TYPE(dd1)
    TYPE IS(ty)
      dd1%num = dd1%num * 2
  END SELECT
END SUBROUTINE
