MODULE mod1
IMPLICIT NONE

REAL :: num

INTERFACE
SUBROUTINE msub(dd1)
  REAL :: dd1
END SUBROUTINE
END INTERFACE

END MODULE


PROGRAM main
USE mod1
IMPLICIT NONE

PROCEDURE(msub) :: prc

INTERFACE gnr
  PROCEDURE prc
END INTERFACE

num = 14.0
CALL gnr(num)

IF(num .EQ. 28.0) THEN
  PRINT*,"PASS"
ELSE
  PRINT*,"ERROR"
END IF

END PROGRAM

SUBROUTINE prc(dd1)
  REAL :: dd1
  dd1 = dd1 * 2.0
END SUBROUTINE
