MODULE mod1
IMPLICIT NONE

PRIVATE :: msub
PROCEDURE(csub),PRIVATE,POINTER :: pprc

INTERFACE gnr
  MODULE PROCEDURE msub
  PROCEDURE pprc
END INTERFACE

CONTAINS
SUBROUTINE csub(dd1)
  REAL :: dd1
  dd1 = 5.0
END SUBROUTINE

SUBROUTINE msub(dd1)
  INTEGER :: dd1
  dd1 = 2
END SUBROUTINE

SUBROUTINE sub()
pprc => csub
END SUBROUTINE

END MODULE


PROGRAM main
USE mod1
IMPLICIT NONE

REAL :: ii

CALL sub()
CALL gnr(ii)

IF(ii .EQ. 5.0) THEN
  PRINT*,"PASS"
ELSE
  PRINT*,"ERROR"
END IF

END PROGRAM


