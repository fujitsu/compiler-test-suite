MODULE mod1
IMPLICIT NONE

PROCEDURE(ss),POINTER :: ptr

INTERFACE
INTEGER FUNCTION ss(dd)
INTEGER :: dd
END FUNCTION
END INTERFACE

END MODULE


PROGRAM main
USE mod1
IMPLICIT NONE

INTEGER :: num = 2,res

ptr => ss
CALL sub(ptr)

CONTAINS
SUBROUTINE sub(dum)
PROCEDURE(ss),POINTER :: dum

INTERFACE gnr
  PROCEDURE :: dum
END INTERFACE

dum => ss
res = gnr(num)

IF(res .EQ. 4) THEN
  PRINT*,"PASS"
ELSE
  PRINT*,"ERROR"
END IF

END SUBROUTINE
END PROGRAM

INTEGER FUNCTION ss(dd)
INTEGER :: dd
ss = dd + 2
END FUNCTION

