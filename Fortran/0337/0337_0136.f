      DOUBLE PRECISION A(9),PAI,DX,DSTAL,DSTTAL,DXA,DXX,DY,DIFF,TES
      TES=0.6366197723675813D0
      A(1)=+1.570796326794896D0
      A(2)=-0.6459640975062462D0
      A(3)=+0.7969262624616563D-1
      A(4)=-0.4681754135304255D-2
      A(5)=+0.1604411847130916D-3
      A(6)=-0.359884302046725D-5
      A(7)=+0.5692136428098D-7
      A(8)=-0.66844168318D-9
      A(9)=+0.587297574D-11
      PAI=3.141592653589793D0
      IX=-2.0*PAI*100.0
      IPAGE=0
      IXL=+2.0*PAI*100.0
C
    5 IPAGE=IPAGE+1
      ITEM=1
      L50=0
      L10=0
C
   20 DXX=IX
      DX=DXX/100.0D0
   21 DSTAL=DSIN(DX)
      DXA=DABS(DX)
C
   25 IF(DXA-PAI/2.0D0) 60,30,30
   30 IF(DXA-PAI) 35,40,40
   35 DXA=PAI-DXA
      GO TO 60
   40 IF(DXA-3.0D0*PAI/2.0D0) 45,50,50
   45 DXA=-1.0D0*(DXA-PAI)
      GO TO 60
   50 IF(DXA-2.0D0*PAI) 55,500,500
   55 DXA=-1.0D0*(2.0D0*PAI-DXA)
      IF (DXA-PAI) 60,25,25
   60 DY=TES*DXA
C
      DSTTAL=0.0D0
      DO 70 K=1,9
   70 DSTTAL=DSTTAL+A(K)*DY**(2*(K-1)+1)
C
      IF (DX) 80,90,90
   80 DSTTAL=-1.0D0*DSTTAL
   90 DIFF=DSTTAL-DSTAL
   95 IF (DABS(DIFF)-1.0D-14) 100,120,120
  100 CONTINUE
C
  110 FORMAT(1H ,5X,8H*OK*    ,4(5X,D24.17) )
      GO TO 135
  120 WRITE(6,130) DX,DSTAL,DSTTAL,DIFF
  130 FORMAT (1H ,5X,8H*ERROR* ,   4(5X,D24.17)  )
  135 GO TO (140,210),ITEM
C
  140 IX=IX+1
      IF(IX-IXL) 160,199,199
  160 L10=L10+1
      IF (L10-10) 20,170,170
  170 L50=L50+1
      IF (L50-5) 175,300,300
  175 CONTINUE
C
  180 FORMAT(1H  )
      L10=0
      GO TO 20
  300 CONTINUE
C
  190 FORMAT(1H1 /)
      GO TO 5
  199 ITEM=ITEM+1
      IPAGE=IPAGE+1
C
C
      DX=-PAI/2.0D0
  210 DX=DX+PAI/4.0D0
      IF (DX-0.01-PAI) 21,21,500
C
  500 CONTINUE
      STOP
      END
