TYPE TY1
     COMPLEX(2)::C2
     REAL(2) :: R2
END TYPE

TYPE(TY1)::OBJ
OBJ%C2=(7,8)
OBJ%R2=5
ASSOCIATE(ASC=>OBJ)
    IF(ASC%C2 .NE. (7,8))PRINT*,"101"
    IF(ASC%R2 .NE. 5)PRINT*,"102"
    IF(KIND(ASC%R2) .NE. 2)PRINT*,"103"
    IF(KIND(ASC%C2) .NE. 2)PRINT*,"104"
    PRINT*,"PASS"
END ASSOCIATE
END
