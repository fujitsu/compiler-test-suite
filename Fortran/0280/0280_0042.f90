 TYPE TY(K)
 INTEGER ,KIND :: K=2
 REAL (KIND=K) :: R
 COMPLEX (KIND=K) :: C
 END TYPE TY

 TYPE :: TY1
 TYPE(TY(2)) :: OBJ
 END TYPE TY1
 TYPE (TY1) :: EOBJ
 REAL(2) :: R2=1.0_2
 COMPLEX(2) :: C2=(1.0_2,1.0_2)
 EOBJ=TY1((TY(2)(1.0_2,(1.0_2,1.0_2))))
 EOBJ=TY1((TY(2)(R2,C2)))
 IF(EOBJ%OBJ%R /= 1.0_2)PRINT*,'101'
 IF(EOBJ%OBJ%C /= (1.0_2,1.0_2))PRINT*,'102'
 PRINT*,'PASS'
 END
