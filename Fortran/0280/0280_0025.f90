TYPE TY
REAL(KIND=2),ALLOCATABLE :: R2
COMPLEX(KIND=2),ALLOCATABLE :: C2(:)
END TYPE
TYPE(TY),ALLOCATABLE :: OBJ1
TYPE(TY) :: OBJ2
ALLOCATE(OBJ2%R2)
ALLOCATE(OBJ2%C2(2))
OBJ2%R2=4
OBJ2%C2=(5,6)
ALLOCATE(OBJ1,SOURCE=OBJ2)
IF(OBJ1%R2 .NE. 4)PRINT*,"101"
IF(ALLOCATED(OBJ1%R2) .EQV. .FALSE.)PRINT*,"102"
IF(ANY(OBJ1%C2 .NE. (5,6)))PRINT*,"103"
IF(ALLOCATED(OBJ1%C2) .EQV. .FALSE.)PRINT*,"104"
PRINT*,"PASS"
END
