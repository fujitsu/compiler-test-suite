SUBROUTINE SUB(DPTR,ADPTR,ADEXP,ADPAR,DANULL,ASIZE)
TYPE(TY),POINTER,INTENT(IN) :: DPTR
REAL(2),POINTER,INTENT(IN) :: ADPTR(:)
REAL(2) :: ADEXP(:)
REAL(2) :: ADPAR(:)
TYPE(*),target :: DANULL(:)

REAL(2) :: ASIZE(..)
IF(SIZE(ASIZE) .NE. 3) PRINT*,"220"
IF(ANY(DPTR%R2CMP .NE. [2,3])) PRINT*,"221"
IF(ANY(ADPTR .NE. [2,3])) PRINT*,"222"
IF(ANY(ADPAR .NE. [2,3])) PRINT*,"223"
IF(ANY(ADEXP .NE. [4,6])) PRINT*,"224"
IF(SIZE(DANULL) .NE. 2) PRINT*,"225"
IF(IS_CONTIGUOUS(DANULL) .NEQV. .FALSE.) PRINT*,"225_1"
DPTR%R2CMP = DPTR%R2CMP + 2
END
SUBROUTINE SUBC(CDPTR)
CLASS(*) :: CDPTR
CLASS(*),POINTER :: CPTR
REAL(2),TARGET:: TRG = 6
CPTR => TRG
SELECT TYPE(CDPTR)
TYPE IS(REAL(KIND=2))
     IF(CDPTR .NE. 3) PRINT*,"302"
     CDPTR = 5
CLASS DEFAULT
  PRINT*,"301"
END SELECT
IF(SAME_TYPE_AS(CPTR,CDPTR) .NEQV. .TRUE.) PRINT*,"302"
END
REAL(2) FUNCTION R2FUN()
  R2FUN = 2
END
