END TYPE

TYPE(TY) :: OBJ
OBJ%R2_AR(3)= 10.0_2
OBJ%RES=OBJ%R2/OBJ%R2
IF(OBJ%RES .NE. (1.0_2/1.0_2))PRINT*,"101"

OBJ%RES_DSB(2)=OBJ%R2_AR(3)/OBJ%R8(1)
