TYPE TY
COMPLEX(8) :: C8(2) = (1.0_8,1.0_8)     
COMPLEX(2) :: C2 = (2.0_2,3.0_2)     
COMPLEX(2) :: CMP
COMPLEX(2) :: C2_AR(2) = (10.0_2,10.0_2)     
COMPLEX(2) :: CMP_DSB(4)

REAL(2) :: R2 = 1.0_2
REAL(2) :: R2_AR(7) = 4.0_2
REAL(8) :: R8(2) = 10.0_8

INTEGER(4) :: I8 = 2_4
INTEGER(2) :: I2(2) = 5_2

END TYPE

TYPE(TY) :: OBJ
INTEGER::I4=2_4
COMPLEX(2)::CC=(2.0_2,3.0_2)
OBJ%CMP=OBJ%C2/OBJ%R2
IF(OBJ%CMP .NE. (2.0_2,3.0_2))PRINT*,"101"

OBJ%CMP_DSB(2)=OBJ%C2_AR(2)/OBJ%R8(1)
IF(OBJ%CMP_DSB(2) .NE. (1.0_2,1.0_2))PRINT*,"102"

OBJ%CMP=OBJ%I8/OBJ%C2
IF(OBJ%CMP .NE. I4/CC)PRINT*,"103"

OBJ%CMP_DSB(3)=OBJ%C2_AR(1)/OBJ%I2(1)
IF(OBJ%CMP_DSB(3) .NE. ((10.0_2,10.0_2)/5))PRINT*,"104"

OBJ%CMP_DSB(4)=OBJ%C2_AR(2)/OBJ%C8(2)
IF(OBJ%CMP_DSB(4) .NE. ((10.0_2,10.0_2)/(1.0_8,1.0_8)))PRINT*,"105"

OBJ%CMP_DSB(1)=OBJ%C2_AR(1)/OBJ%R2_AR(2)
IF(OBJ%CMP_DSB(1) .NE. ((10.0_2,10.0_2)/4.0_2))PRINT*,"106"
PRINT*,"PASS"
END
