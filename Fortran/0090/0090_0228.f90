      REAL   ,DIMENSION(10)::C,D

      C=(/(I,I=1,10)/)
      D=(/(I*I,I=1,10)/)

      C(2:6)=C(1:5)+D(1:5) ! -1
                           !  1     => X
      C(2:6)=C(1:5)+D(1:5) ! -1
                           ! -1, 1  => X
      D(2:6)=D(1:5)+C(1:5) ! -1
                           ! -1, -1 => O
      C(2:6)=D(3:7)+C(1:5) ! -1
                           ! -1, 1  => X
      C(3:7)=D(3:7)+C(1:5) ! -1 
                           !  1, 1  => X
      D(2:6)=D(1:5)+C(1:5) ! -1
                           ! 1,-1   => X
      D(2:6)=D(1:5)+C(1:5) ! -1
                           ! 0,-1   => O
      D(2:6)=D(2:6)+C(3:7) !  0
                           ! 0,-1   => O
      C(3:7)=D(3:7)+C(1:5) !  0
                           ! 0,1    => O
      D(2:6)=D(2:6)+C(3:7) !  0

      PRINT *,C
      PRINT *,D

      END
