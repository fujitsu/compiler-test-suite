      program main
      complex*8  x0(10,10),x1(10,10),x2(10,10),
     &        x3(10,10),x4(10,10),x5(10,10),
     &        x6(10,10),x7(10,10),x8(10,10),
     &        x9(10,10),x10(10,10),x11(10,10),
     &        y0(10,10),y1(10,10),y2(10,10),
     &        y3(10,10),y4(10,10),y5(10,10),
     &        y6(10,10),y7(10,10),y8(10,10),
     &        y9(10,10),y10(10,10),y11(10,10),
     &        z0(10,10),z1(10,10),z2(10,10),
     &        z3(10,10),z4(10,10),z5(10,10),
     &        z6(10,10),z7(10,10),z8(10,10),
     &        z9(10,10),z10(10,10),z11(10,10)
      complex*8  res,result
      data    result/(200.000000,-5400.00000)/


      do j=1,10
       do i=1,10
         x1(i,j)=(1.0,-1.0)
         y1(i,j)=(2.0,-1.0)
         z1(i,j)=(3.0,-1.0)
         x2(i,j)=(4.0,-1.0)
         y2(i,j)=(5.0,-1.0)
         z2(i,j)=(6.0,-1.0)
         x3(i,j)=(7.0,-1.0)
         y3(i,j)=(8.0,-1.0)
         z3(i,j)=(9.0,-1.0)
         x4(i,j)=(1.0,-2.0)
         y4(i,j)=(2.0,-2.0)
         z4(i,j)=(3.0,-2.0)
         x5(i,j)=(4.0,-2.0)
         y5(i,j)=(5.0,-2.0)
         z5(i,j)=(6.0,-2.0)
         x6(i,j)=(7.0,-2.0)
         y6(i,j)=(8.0,-2.0)
         z6(i,j)=(9.0,-2.0)
         x7(i,j)=(1.0,-3.0)
         y7(i,j)=(2.0,-3.0)
         z7(i,j)=(3.0,-3.0)
         x8(i,j)=(4.0,-3.0)
         y8(i,j)=(5.0,-3.0)
         z8(i,j)=(6.0,-3.0)
         x9(i,j)=(7.0,-3.0)
         y9(i,j)=(8.0,-3.0)
         z9(i,j)=(9.0,-3.0)
         x10(i,j)=(10.0,-4.0)
         y10(i,j)=(11.0,-4.0)
         z10(i,j)=(12.0,-4.0)
         x11(i,j)=(13.0,-4.0)
         y11(i,j)=(14.0,-4.0)
         z11(i,j)=(15.0,-4.0)
       enddo
      enddo
      do j=1,10
       do i=1,10
         x0(i,j)=x1(i,j)*(x2(i,j)+x3(i,j)
     &                   +x4(i,j)+x5(i,j))
     &          +x6(i,j)*(x7(i,j)+x8(i,j)
     &                   +x9(i,j)+x10(i,j))
     &          +x11(i,j)
         y0(i,j)=y1(i,j)*(y2(i,j)-y3(i,j)
     &                   -y4(i,j)-y5(i,j))
     &          +y6(i,j)*(y7(i,j)-y8(i,j)
     &                   -y9(i,j)-y10(i,j))
     &          +y11(i,j)
         z0(i,j)=z1(i,j)*(z2(i,j)+z3(i,j)
     &                   -z4(i,j)+z5(i,j))
     &          +z6(i,j)*(z7(i,j)-z8(i,j)
     &                   +z9(i,j)-z10(i,j))
     &          +z11(i,j)
       enddo
      enddo
      res=0

      do j=1,10
       do i=1,10
         res=res+x0(i,j)+y0(i,j)+z0(i,j)
       enddo
      enddo

      if (abs(res-result)>0.00001) goto 10
      write (6,*) "ok"
      goto 20
 10   write (6,*) "ng"
      write(6,*) res
 20   continue
      end
