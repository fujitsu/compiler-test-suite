IMPLICIT NONE
INTEGER::DD(5,5)
TYPE TY
 INTEGER::SS(2)
END TYPE
 TYPE(TY)::OBJ

INTEGER,POINTER::PTR
INTEGER,TARGET::T1=20
DATA ((DD(KK,JJ),INTEGER::KK=1,5),INTEGER*8::JJ=1,5),PTR /25*55,T1/
IF(ANY(DD /= 55))PRINT*,101

DATA (OBJ%SS(KK),INTEGER(1+1+2)::KK=1,2) /60,50/
IF(OBJ%SS(1) /= 60)PRINT*,102
IF(OBJ%SS(2) /= 50)PRINT*,103
IF(ASSOCIATED(PTR).neqv..TRUE.)print*,"104"
IF(PTR.NE.20)PRINT*,"105"
PRINT*,"PASS"
END
