   IF (ABS(B-2.00000000)>1.0E-06) PRINT *,"NG B=",B
   IF (K.NE.7) PRINT *,"NG K=",K
   END
   SUBROUTINE JSUB(X,Y,J)
   INCLUDE "000.h"
   PARAMETER (Q=1.0D0,P=2.0D0)
   N=3
   M=4
   X=Q
   Y=P
   J=N+M
   END
