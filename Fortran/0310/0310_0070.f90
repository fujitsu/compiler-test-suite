   CALL JSUB (A,B,K)
   IF (K.NE.0) GO TO 10
10 CONTINUE
   Q=3.0D0;P=4.0D0
   IF (N.EQ.Q.AND.M.EQ.P) PRINT *,"*** OK ***"
