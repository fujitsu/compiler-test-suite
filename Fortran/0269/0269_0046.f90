 REAL(II,II)=II+II
 END DO
 
 IF(INTEGER(4,4)/=8)PRINT*,102
 IF(INTEGER(3,3)/=6)PRINT*,103
 IF(ANY(INTEGER /= REAL))PRINT*,101

