TYPE POINT
REAL :: X, Y
END TYPE POINT
TYPE, EXTENDS(POINT) :: POINT_3D
REAL :: Z
END TYPE POINT_3D
TYPE, EXTENDS(POINT) :: COLOR_POINT
INTEGER :: COLOR
END TYPE COLOR_POINT
TYPE(COLOR_POINT), TARGET :: C
CLASS(POINT), POINTER :: P_OR_C
P_OR_C => C
SELECT TYPE ( A => P_OR_C )
 CLASS IS (POINT_3D)
   PRINT *, A%X, A%Y,"102" ! This block gets executed
 TYPE IS (POINT_3D)
  PRINT *, A%X, A%Y,"101"
 CLASS DEFAULT
SELECT TYPE ( A => P_OR_C )
