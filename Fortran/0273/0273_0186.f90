MODULE M
IMPLICIT NONE
INTERFACE TESTIF
  MODULE PROCEDURE TEST1
  MODULE PROCEDURE TEST2
END INTERFACE
CONTAINS
REAL FUNCTION TEST1 (OBJ)
 REAL*2 :: OBJ
 TEST1 = OBJ
END FUNCTION
REAL FUNCTION TEST2 (OBJ2)
  REAL*4 :: OBJ2
  TEST2 = OBJ2+1
END FUNCTION
END MODULE

PROGRAM TEST
USE M
IMPLICIT NONE
IF(TESTIF(2.0_2) /= 2.0) PRINT *,101
IF(TESTIF(3.0_4) /= 4.0) PRINT *,102
PRINT *,"PASS"
END PROGRAM

