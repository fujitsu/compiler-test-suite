TYPE TY
COMPLEX(KIND=2),ALLOCATABLE :: C1
COMPLEX(KIND=2),ALLOCATABLE :: C2(:)
COMPLEX(KIND=2) :: SRC(2)
END TYPE
TYPE(TY) :: OBJ
OBJ%SRC=(5,6)
ALLOCATE(COMPLEX(KIND=2)::OBJ%C1)
ALLOCATE(OBJ%C2,SOURCE=OBJ%SRC)
IF(ALLOCATED(OBJ%C1) .EQV. .FALSE.)PRINT*,"102"
IF(ANY(OBJ%C2 .NE. (5,6)))PRINT*,"102"
IF(ALLOCATED(OBJ%C2) .EQV. .FALSE.)PRINT*,"103"
PRINT*,"PASS"
END
