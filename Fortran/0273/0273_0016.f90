REAL*2,PARAMETER,DIMENSION(5) ::ARR=[1.1_2,2.1_2,3.1_2,4.1_2,5.1_2]
COMPLEX(KIND=2),PARAMETER::RR=CMPLX(ARR(1),ARR(2))
COMPLEX(KIND=2),PARAMETER::RR4=CMPLX(ARR(3),ARR(4))
REAL*2,PARAMETER::RR2=AIMAG(RR)
REAL*2,PARAMETER::RR3=AIMAG(RR4)
REAL*2,PARAMETER::RR5=AIMAG(CMPLX(ARR(2)+ARR(1),ARR(3)*ARR(5)/ARR(4)))
IF(RR2 /= 2.1_2)PRINT*,"101"
IF(RR3 /= 4.1_2)PRINT*,"102"
IF(RR5 /= 3.85546875)PRINT*,"103"
PRINT*,"PASS"
END
