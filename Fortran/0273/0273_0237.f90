MODULE MOD
TYPE TY
  REAL*2:: X
END TYPE TY

INTERFACE OPERATOR(+)
 FUNCTION FUN (PX,AX) RESULT (C)
  IMPORT TY
  TYPE(TY), INTENT(IN) :: PX 
  REAL*2,INTENT(IN)::AX
  TYPE(TY) :: C
  END FUNCTION 
END INTERFACE
END MODULE 

PROGRAM MAIN
USE MOD
 TYPE(TY) ::OBJ1,OBJ2
 REAL*2::R2
 OBJ2=TY(5.0)
 R2=6.0
 OBJ1=OBJ2+R2
 IF(OBJ1%X .NE. 11)PRINT*,"101"
 PRINT*,"PASS"
END
  
 FUNCTION FUN (PX,AX) RESULT (C)
  USE MOD
  TYPE(TY), INTENT(IN) :: PX 
  REAL*2,INTENT(IN)::AX
  TYPE(TY) :: C
  C%X=PX%X + AX
  END FUNCTION 
