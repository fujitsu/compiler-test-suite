TYPE TY

COMPLEX(8) :: C8(5) = (1,1)     
COMPLEX(2) :: C2 = (2,3)     
COMPLEX(2) :: C2_AR(6) = (10,10)     

REAL(2) :: RES
REAL(2) :: RES_DSB(4)

REAL(2) :: R2= 1.0
REAL(2) :: R2_AR(4) = 4
REAL(8) :: R8(1) = 10.0

INTEGER(4) :: I4(2) = 5
INTEGER(8) :: I8 = 2

END TYPE

TYPE(TY) :: OBJ

OBJ%RES=OBJ%R2/OBJ%R2
IF(OBJ%RES .NE. (1.0/1.0))PRINT*,"101"

OBJ%RES_DSB(2)=OBJ%R2_AR(3)/OBJ%R8(1)
IF(abs(OBJ%RES_DSB(2) -    (4/10.0))>0.01)PRINT*,"102"

OBJ%RES=OBJ%I8/OBJ%R2
IF(OBJ%RES .NE. (2/(1.0)))PRINT*,"103"

OBJ%RES_DSB(3)=OBJ%R2_AR(1)/OBJ%I4(1)
IF(abs(OBJ%RES_DSB(3) -    (4./5))>0.05)PRINT*,"104",real(OBJ%RES_DSB(3),4)

OBJ%RES_DSB(4)=OBJ%C2_AR(2)/OBJ%R2_AR(2)
IF(OBJ%RES_DSB(4) .NE. REAL((10,10)/(4)))PRINT*,"105"

OBJ%RES=OBJ%R2/OBJ%C2
IF(abs(OBJ%RES -    REAL((1.0)/(2,3)))>0.01)PRINT*,"106"

PRINT*,"PASS"
END
