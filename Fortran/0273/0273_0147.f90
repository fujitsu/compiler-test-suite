TYPE TY
COMPLEX(8) :: C8(2) = (1,1)     
COMPLEX(2) :: C2 = (2,3)     
COMPLEX(2) :: CMP
COMPLEX(2) :: C2_AR(2) = (10,10)     
COMPLEX(2) :: CMP_DSB(4)

REAL(2) :: R2 = 1.0
REAL(2) :: R2_AR(7) = 4
REAL(8) :: R8(2) = 10.0

INTEGER(4) :: I8 = 2
INTEGER(2) :: I2(2) = 5

END TYPE

TYPE(TY) :: OBJ
OBJ%CMP=OBJ%C2/OBJ%R2
IF(OBJ%CMP .NE. (2,3))PRINT*,"101"

OBJ%CMP_DSB(2)=OBJ%C2_AR(2)/OBJ%R8(1)
IF(OBJ%CMP_DSB(2) .NE. (1,1))PRINT*,"102"

OBJ%CMP=OBJ%I8/OBJ%C2
IF(abs(OBJ%CMP -    (2/(2,3)))>0.01)PRINT*,"103"

OBJ%CMP_DSB(3)=OBJ%C2_AR(1)/OBJ%I2(1)
IF(OBJ%CMP_DSB(3) .NE. ((10,10)/5))PRINT*,"104"

OBJ%CMP_DSB(4)=OBJ%C2_AR(2)/OBJ%C8(2)
IF(OBJ%CMP_DSB(4) .NE. ((10,10)/(1,1)))PRINT*,"105"

OBJ%CMP_DSB(1)=OBJ%C2_AR(1)/OBJ%R2_AR(2)
IF(OBJ%CMP_DSB(1) .NE. ((10,10)/4))PRINT*,"106"
PRINT*,"PASS"
END
