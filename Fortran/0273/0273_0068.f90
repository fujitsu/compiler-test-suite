REAL*2,POINTER::R2
REAL*2,POINTER::RR2(:)
COMPLEX(KIND=2),POINTER::C2
COMPLEX(KIND=2),POINTER::CC2(:)
REAL*2,TARGET::TR2
REAL*2,TARGET::TRR2(4)
COMPLEX(KIND=2),TARGET::TC2
COMPLEX(KIND=2),TARGET::TCC2(4)
TR2=5.5_2
TRR2=TR2
TC2=(4.5_2,3.5_2)
TCC2=TC2

R2=>TR2
RR2=>TRR2
C2=>TC2
CC2=>TCC2
IF(ASSOCIATED(R2) .NEQV. .TRUE.)PRINT*,"101"
IF(ASSOCIATED(RR2) .NEQV. .TRUE.)PRINT*,"102"
IF(ASSOCIATED(C2) .NEQV. .TRUE.)PRINT*,"103"
IF(ASSOCIATED(CC2) .NEQV. .TRUE.)PRINT*,"104"

IF(R2 /= TR2) PRINT*,"201"
IF(C2 /= TC2) PRINT*,"202"
IF(ANY(RR2 /= TRR2)) PRINT*,"203"
IF(ANY(CC2 /= TCC2)) PRINT*,"204"

PRINT*,"PASS"
END




