       DATA A11/10*2.,10*5.,10*4.,10*3.,10*4./
       DATA A12/10*1.,10*6.,10*5.,10*2.,10*3./
       DATA A14/100*2.,100*3.,100*4.,100*5.,100*6./

       DO 101 J = 1 , 10
       DO 101 I = 1 , 50
