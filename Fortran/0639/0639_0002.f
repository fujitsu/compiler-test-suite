         A10( I) = A10(I)  + DDD(I)
         X10( I) = A11(I) - CCC(I)
         X11( I) = A14(I,J) - CCC(I)
         A11( I) = A12(50) + I + DDD(I)
         A12( I) = CCC( I) + DDD(I)
         A13( I) = BBB(I) + I
         A13( I) = A13( I) + CCC(I) - DDD(I) + A13(I)
         X12(I,J) = X10( I) + I + DDD(I)
 101   CONTINUE
