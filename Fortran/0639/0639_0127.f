           REAL*8  A(50),B(50),C(50),D(50),E(50),F(50),G(50),H(50),
     1             O(50),P(50),Q(50),R(50),S(50),T(50),U(50),V(50),
     2             CT(5),AY(5),X,Y,Z

           DATA A/10*1.0,10*2.0,10*3.0,20*4.0/
           DATA B/10*1.0,10*2.0,20*3.0,10*4.0/
           DATA C/10*1.0,20*2.0,10*3.0,10*4.0/
           DATA D/20*1.0,10*2.0,10*3.0,10*4.0/
           DATA E/20*4.0,10*3.0,10*2.0,10*1.0/
           DATA F/10*4.0,20*3.0,10*2.0,10*1.0/
           DATA G/10*4.0,10*3.0,20*2.0,10*1.0/
           DATA H/10*4.0,10*3.0,10*3.0,20*1.0/
           DATA O/10*3.0,10*4.0,20*1.0,10*2.0/
           DATA P/10*3.0,20*4.0,10*1.0,10*2.0/
           DATA Q/20*3.0,10*4.0,10*1.0,10*2.0/
           DATA R/20*2.0,10*3.0,10*4.0,10*1.0/
           DATA S/10*2.0,20*3.0,10*4.0,10*1.0/
           DATA T/10*2.0,10*3.0,20*4.0,10*1.0/
           DATA U/10*2.0,10*3.0,10*4.0,20*1.0/
           DATA V/10*4.0,10*2.0,10*3.0,20*1.0/
           DATA X,Y,Z/3*10.0/
           DATA AY/97.0,61.0,89.0,73.0,77.0/
           CT(1) = 59.0
           CT(2) = 37.0
           CT(3) = 15.0
           CT(4) = 93.0
           CT(5) = 71.0

           DO 10 I = 1 , 50
               B(I) = (CT(4)-AY(4))/A(I)/X*Z*A(I)*AY(4)
               C(I) = A(I)/Z*CT(3)/(AY(3)+Y)*A(I)*CT(3)
               D(I) = C(I)/CT(2)/B(I)/AY(3)*Y*A(I)*Z
               V(I) = G(I)/AY(2)+Y/CT(2)/F(I)*A(I)*AY(2)
               T(I) = (Z-AY(5))/A(I)/CT(5)/V(I)/(AY(4)-Y)/CT(4)/A(I)/X
               Q(I) = CT(1)*AY(3)/S(I)/X/R(I)/(CT(2)+Z)/AY(2)*A(I)*Y
               P(I) = AY(1)/Y/T(I)/AY(2)/Q(I)/Z/CT(1)/X/A(I)/CT(1)
               U(I) = O(I)/CT(1)*AY(1)/X/H(I)/CT(2)*A(I)*AY(1)*B(I)*Y
               E(I) = U(I)/X/V(I)/AY(1)/A(I)/CT(1)/B(I)/AY(1)/C(I)/X
 10        CONTINUE

           WRITE(6,100) B , C , D
           WRITE(6,100) V , T , Q
           WRITE(6,100) P , U , E
 100       FORMAT(3F12.6)
           STOP
           END
