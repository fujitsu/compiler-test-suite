           REAL*8 A(100),B(100),C(100),D(100),
     1            A1(100),B1(100),C1(100),D1(100),
     2            AA(100),BB(100),CC(100),DD(100),
     3            A2(100),B2(100),C2(100),D2(100),
     4            CN(3),O(3),X,Y,Z

           DATA A/40*1.0D0,30*2.0D0,20*3.0D0,10*4.0D0/
           DATA B/30*1.0D0,20*2.0D0,10*3.0D0,40*4.0D0/
           DATA C/20*1.0D0,10*2.0D0,40*3.0D0,30*4.0D0/
           DATA D/10*1.0D0,40*2.0D0,30*3.0D0,20*4.0D0/
           DATA A1/40*1.0D0,30*2.0D0,20*3.0D0,10*4.0D0/
           DATA B1/30*1.0D0,20*2.0D0,10*3.0D0,40*4.0D0/
           DATA C1/20*1.0D0,10*2.0D0,40*3.0D0,30*4.0D0/
           DATA D1/10*1.0D0,40*2.0D0,30*3.0D0,20*4.0D0/
           DATA AA/40*1.0D0,30*2.0D0,20*3.0D0,10*4.0D0/
           DATA BB/30*1.0D0,20*2.0D0,10*3.0D0,40*4.0D0/
           DATA CC/20*1.0D0,10*2.0D0,40*3.0D0,30*4.0D0/
           DATA DD/10*1.0D0,40*2.0D0,30*3.0D0,20*4.0D0/
           DATA A2/40*1.0D0,30*2.0D0,20*3.0D0,10*4.0D0/
           DATA B2/30*1.0D0,20*2.0D0,10*3.0D0,40*4.0D0/
           DATA C2/20*1.0D0,10*2.0D0,40*3.0D0,30*4.0D0/
           DATA D2/10*1.0D0,40*2.0D0,30*3.0D0,20*4.0D0/
           DATA O/10.0D0,20.0D0,30.0D0/
           DATA X,Y,Z/3*2.0D0/
           CN(1) = 3.0D0
           CN(2) = 5.0D0
           CN(3) = 7.0D0

           DO 20 I = 1 , 100
               B(I) = CN(2)/D(I)/O(1)/C(I)/X/D(I)/CN(3)/C(I)/O(2)
               A(I) = O(3)/B(I)/Y/C(I)/CN(1)/D(I)/O(2)/C(I)/Z/D(I)/CN(3)
               B1(I) =CN(2)/C1(I)/O(1)/D1(I)/X/C1(I)/CN(1)/D1(I)/O(3)
               A1(I) = O(3)/D1(I)/Y/C1(I)/CN(1)/B1(I)/O(2)/D1(I)/X

             DO 10 J = 1 , 100
                 CC(J) = X/DD(J)/CN(2)/DD(J)/O(3)
                 BB(J) = CN(1)/CC(J)/O(1)/DD(J)/Z
                 AA(J) = O(1)/BB(J)/X/CC(J)/CN(3)/DD(J)/O(2)
                 C2(J) = X/D2(J)/CN(2)/D2(J)/O(3)
                 B2(J) = CN(1)/D2(J)/O(1)/C2(J)/Z
                 A2(J) = O(1)/D2(J)/X/C2(J)/CN(3)/B2(J)/O(2)
 10          CONTINUE
 20        CONTINUE

           WRITE(6,100) B , A
           WRITE(6,100) B1 , A1
           WRITE(6,200) CC , BB , AA
           WRITE(6,200) C2 , B2 , A2
 100       FORMAT(1H 2D13.7)
 200       FORMAT(1H 3D13.7)
           STOP
           END
