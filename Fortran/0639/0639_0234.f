         REAL A(1024),B(1024),C(1024)
         REAL X(1024),Y(1024)
         INTEGER IA(1024),IB(1024)

      DATA IA/100*10,100*8,100*6,100*4,100*2,
     E         100*1,100*3,100*5,100*7,100*9,24*1/
      DATA  A/100*10.,100*8.,100*6.,100*4.,100*2.,100*1.,
     E        100*3.,100*5.,100*7.,100*9.,24*1./
      DATA  B/100*1.,100*3.,100*5.,100*0.,100*6.,100*9.,
     E        100*3.,100*3.,100*4.,100*2.,24*1./

         DO 10 I = 1 , 1024
           Y(I) = I
           X(I) = I + 10
           C(I) = FLOAT(I)
 10      CONTINUE

         DO 11 I = 1 , 1024
           X(I)  = A( IA(I) ) - A(I)
           IB(I) = ABS( X(I) )
           IF( IB(I) .NE. 0 ) THEN
              Y(I) = B( IB(I) ) + C(I)
           ELSE
              IB(I) = X(I) + 1.0
              C(I)  = A( IB(I) ) - I - A(I)
              IB(I) = ABS( X(I) ) + 1
              Y(I)  = B( IB(I) ) + C(I)
           ENDIF
 11      CONTINUE
         PRINT *,A,Y
         END
