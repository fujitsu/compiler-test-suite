           REAL   A1(50),B1(50),C1(50),D1(50),X1,Y1(50)
           REAL*8        B2(50),C2(50),D2(50),X2,Y2(50)
           REAL   A2(50),A3(50),A4(50),A5(50)
           DATA B1/10*1.,10*2.,10*3.,10*4.,10*5./
           DATA C1/10*1.,10*2.,10*3.,10*4.,10*5./
           DATA D1/10*10.,10*20.0,10*30.,10*40.,10*50./
           DATA Y1/10*1.,10*2.,10*3.,10*4.,10*5./
           DATA X1/3.0/
           DATA A2/10*1.0D0,10*2.0D0,10*3.0D0,10*4.0D0,10*5.0D0/
       DATA B2/10*12.0D0,10*24.0D0,10*36.0D0,10*48.0D0,10*60.0D0/
           DATA C2/10*1.0D0,10*2.0D0,10*3.0D0,10*4.0D0,10*2.0D0/
           DATA D2/10*1.0D0,10*2.0D0,10*3.0D0,10*4.0D0,10*2.0D0/
           DATA Y2/10*1.0D0,10*2.0D0,10*3.0D0,10*4.0D0,10*2.0D0/
           DATA X2/6.0D0/
           COMPLEX CC

           J=1
           M=10
           SM=4.
           RR=4.
           CC=(2.,3.)

           DO 10 I = 1 , 50
               A1(I) = B1(I) + X1 + C1(I) - Y1(J) + D1(I) + 1.0
               A2(I) = B2(I) - X2 + C2(I) + Y2(M) + D2(I) - 2.0D0
               A3(I) = B2(I) + X1 + C1(I) * Y2(J) + D1(I) + 2.0D0
               A4(I) = B2(I) / J  + C1(I) - Y2(M) + D1(I) / M
               A5(I) = B1(I) + CC + C2(I) * Y1(J) + D2(I) * RR
               SM = SM + I
 10        CONTINUE

           WRITE(6,11) A1
           WRITE(6,11) A2
           WRITE(6,11) A3
           WRITE(6,11) A4
           WRITE(6,11) A5
 11        FORMAT(1H ,4(E13.6,4X))
           END
