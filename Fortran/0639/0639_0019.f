       COMPLEX*8  A1(50),B1(50),C1(50),D1(50),X1,Y1(50)
       COMPLEX*8  A2(50),B2(50),C2(50),D2(50),X2,Y2(50),A3(50)
       COMPLEX*8  A4(50),A5(50)
           DATA A1/10*(1.,1.),10*(2.,1.),10*(3.,4.),20*(1.,4.)/
           DATA B1/10*(1.,2.),10*(3.,2.),10*(4.,3.),20*(4.,2.)/
           DATA C1/10*(3.,1.),10*(2.,4.),10*(3.,4.),20*(4.,2.)/
           DATA D1/10*(10.,4.),10*(50.,2.),10*(40.,3.),20*(60.,4.)/
           DATA Y1/10*(4.,2.),10*(2.,1.),10*(2.,3.),20*(4.,2.)/
           DATA X1/(3.0,2.0)/
           DATA A2/10*(1.,4.),10*(2.,2.),10*(3.,3.),20*(1.,4.)/
           DATA B2/10*(3.,1.),10*(2.,2.),10*(3.,3.),20*(4.,2.)/
           DATA C2/10*(1.,4.),10*(2.,2.),10*(3.,3.),20*(3.,1.)/
           DATA D2/10*(2.,1.),10*(2.,2.),10*(3.,3.),20*(5.,4.)/
           DATA Y2/10*(1.,2.),10*(2.,4.),10*(1.,3.),20*(4.,1.)/
           DATA X2/(6.0,2.0)/
           COMPLEX CC

           J=1
           M=10
           SM=4.
           RR=4.
           CC=(2.,3.)

           DO 10 I = 1 , 50
               A1(I) = B1(I) + X1 + C1(I) - Y1(J) + D1(I) + 1.0
               A2(I) = B2(I) - X2 + C2(I) + Y2(M) + D2(I) - 2.0D0
               A3(I) = B2(I) + X1 + C1(I) * Y2(J) + D1(I) + 2.0D0
               A4(I) = B2(I) / J  + C1(I) - Y2(M) + D1(I) / M
               A5(I) = B1(I) + CC + C2(I) * Y1(J) + D2(I) * RR
               SM = SM + I
 10        CONTINUE

           WRITE(6,12) A1
           WRITE(6,12) A2
           WRITE(6,12) A3
           WRITE(6,12) A4
           WRITE(6,12) A5
           PRINT *,    SM
 12        FORMAT(1H ,2(E13.6,4X,E13.6))
           END
