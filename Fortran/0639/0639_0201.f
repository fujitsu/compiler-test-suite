      PROGRAM MAIN

      REAL   *4  R041(11),R042(11),R043(11),R044(11)
      DATA       R041/01,02,03,04,05,06,07,08,09,10,11/
      DATA       R042/11,10,09,08,07,06,05,04,03,02,01/

      DO 11 K1=1,11
        R041(K1)= INT(K1 * 2  + K1 * 0.2)
        R042(K1)= INT(-K1 * 2.2         )
        IF( MOD(K1,2) .EQ. 1) THEN
          R041(K1)= -R041(K1) * K1
          R042(K1)= +R042(K1) * K1
                              ELSE
          R041(K1)= R041(K1-1)
          R042(K1)= R042(K1-1)
        ENDIF
        R043(K1) = ABS(R041(K1) - R042(K1))
11      R044(K1) = ABS(R041(K1)) - ABS(R042(K1))

      WRITE(6,100) (R041(K1),K1=1,11)
      WRITE(6,100) (R042(K1),K1=1,11)
      WRITE(6,100) (R043(K1),K1=1,11)
      WRITE(6,100) (R044(K1),K1=1,11)
      STOP

100   FORMAT(1H0,5X,
     1      /1H ,5X,4E15.7 )
      END
