           REAL*8 A(50),B(50),C(50),D(50),E(50),F(50),G(50),H(50),
     1            O(50),P(50),Q(50),
     2            CT(3),AY(3),X,Y,Z

           DATA A/10*2.0D0,10*3.0D0,10*2.0D0,20*1.0D0/
           DATA B/10*2.0D0,10*3.0D0,20*2.0D0,10*1.0D0/
           DATA C/10*2.0D0,20*3.0D0,10*2.0D0,10*1.0D0/
           DATA D/20*2.0D0,10*3.0D0,10*2.0D0,10*1.0D0/
           DATA E/10*2.0D0,10*2.0D0,10*3.0D0,20*4.0D0/
           DATA F/10*2.0D0,10*2.0D0,20*3.0D0,10*4.0D0/
           DATA G/10*2.0D0,20*2.0D0,10*3.0D0,10*4.0D0/
           DATA H/20*2.0D0,10*2.0D0,10*3.0D0,10*4.0D0/
           DATA O/10*2.0D0,10*2.0D0,20*3.0D0,10*4.0D0/
           DATA P/10*2.0D0,20*2.0D0,10*3.0D0,10*4.0D0/
           DATA Q/20*2.0D0,10*2.0D0,10*3.0D0,10*4.0D0/
           DATA AY/2.0D0,2.0D0,2.0D0/
           DATA X,Y,Z/3*1.0D0/
           CT(1) = 7.0D0
           CT(2) = 5.0D0
           CT(3) = 3.0D0

           DO 10 I = 1 , 50
               D(I) = (CT(2)-CT(1))/C(I)/X/C(I)/(AY(1)+AY(2))
               E(I) = (CT(3)-X)/B(I)/A(I)/(CT(3)+AY(1))
               F(I) = D(I)/CT(2)*Z/C(I)/(AY(3)-X)
               H(I) = F(I)*CT(2)*E(I)/CT(1)/X/(Y+CT(1))
               O(I) = (CT(2)+AY(3))/A(I)/Y/B(I)/(Z-CT(1))
               P(I) = C(I)/(AY(2)-Y)/D(I)/CT(3)*X/A(I)/CT(2)/B(I)/Z
               Q(I) = E(I)*F(I)/(Z+AY(1))/G(I)/CT(2)/Y/A(I)/AY(1)/B(I)
 10        CONTINUE

           WRITE(6,100) D , E , F , H
           WRITE(6,200) O , P , Q
 100       FORMAT(1H 4F12.6)
 200       FORMAT(1H 3F12.6)
           STOP
           END
