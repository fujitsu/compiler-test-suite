MODULE MOD06
  IMPLICIT NONE

  TYPE :: D06
   CONTAINS
     PROCEDURE, NOPASS :: PROC => SUB06
  END TYPE D06

CONTAINS
  SUBROUTINE SUB06()
    IMPLICIT NONE
    write(9,*) "06"
  END SUBROUTINE SUB06
END MODULE MOD06

SUBROUTINE SELECT_TYPE06()
  USE MOD06

  type(d06), POINTER :: NTYPE_VAR(:)

k=1
  ALLOCATE( NTYPE_VAR(k))

     CALL NTYPE_VAR(1)%PROC()

END SUBROUTINE SELECT_TYPE06

PROGRAM MAIN
  CALL SELECT_TYPE06()
rewind 9
read(9,*) k
if (k/=6) print *,101
  print *, "pass"
END PROGRAM MAIN
