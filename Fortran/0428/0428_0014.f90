MODULE MOD06
  IMPLICIT NONE

  TYPE :: D06
   integer::x
   CONTAINS
     PROCEDURE, NOPASS :: PROC => SUB06
  END TYPE D06

CONTAINS
  SUBROUTINE SUB06()
    IMPLICIT NONE
    write(1,*) 6
  END SUBROUTINE SUB06
END MODULE MOD06

SUBROUTINE SELECT_TYPE06()
  USE MOD06
  IMPLICIT NONE

  TYPE(D06), POINTER :: NTYPE_VAR(:,:)

  ALLOCATE(D06 :: NTYPE_VAR(1,2))

     CALL NTYPE_VAR(1,2)%PROC()

END SUBROUTINE SELECT_TYPE06

