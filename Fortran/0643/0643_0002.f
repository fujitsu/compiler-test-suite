      real*4 anint_1,x,const,tmp
      parameter(const=1.5e0*2.0e0**23)
      if(abs(x).le.2.0e0**22) then
        tmp=(x-const)+const
        if(abs(x-tmp).ne.0.5) then
          anint_1=tmp
        else
          anint_1=anint(x)
        end if
      else
        anint_1=anint(x)
      end if
      end

