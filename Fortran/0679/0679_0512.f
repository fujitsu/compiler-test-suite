      program main
      real   x(30),y(30)
      data x,y/60*1/
      integer n/30/
      real  ax(30)
      data ax / 1.00000000,0.270905793,0.956375957,0.762558460,
     c         -0.132351756,-0.905578375,-0.846220434,
     c         -8.85130931e-03,0.836655617,0.912945271,
     c          0.149877205,-0.750987232,-0.961397469,
     c         -0.287903309,0.650287867,0.990607381,
     c          0.420167029,-0.536572933,-0.999990225,
     c          -0.544021130,0.412118495,0.989358246,
     c          0.656986594,-0.279415488,
     c          -0.958924294,-0.756802499,
     c          0.141120002,0.909297407,0.841470957,1.0/
      do 30 i=2,n-1
       x(i) = y(i) * (n-i)
       call ichi(x,y)
       x(i) = sin(x(i))
   30 continue

      do i=1,30
	if( abs((x(i)-ax(i))/x(i)) .gt. 0.1e-5 ) print *,x(i)
	if( abs((y(i)-1.0)/y(i)) .gt. 0.1e-5 ) print *,y(i)
      end do
      print *,' PASS '
      end
      subroutine ichi(x,y)
      real   x(30),y(30)
      x(1) = y(4)
      end
