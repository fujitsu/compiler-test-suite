      PROGRAM MAIN
      REAL       REAL_A1(10),REAL_B1(10)
      REAL       REAL_A2(10,10),REAL_B2(10,10)
      REAL       REAL_DUMMY(1,10)
      REAL,ALLOCATABLE:: REAL_ALLOC(:,:)
C
      DATA REAL_A1,REAL_A2/110*1.0/
      DATA REAL_B1,REAL_B2/110*1.0/
C
      REAL_A2(1,1:)=REAL_A1*3.0
      CALL SUB(REAL_A1+1.0,REAL_A1)
      REAL_A2(1,:)=REAL_A1+REAL_A2(1,:)
      REAL_A2(1,:)=REAL_A1+REAL_A2(2,:)
      WRITE(6,10) REAL_A1+1.0
   10 FORMAT(1H ,'*** START ***',
     1                    /,1H ,'WRITE',10(1H ,F4.1))
      REAL_A2(1,:)=REAL_A1+REAL_A2(1,:)
      ALLOCATE (REAL_ALLOC(1,1:10))
      REAL_ALLOC(1,:)=REAL_A1+REAL_A2(1,1:10)
      REAL_A2(1,:)=REAL_ALLOC(1,:)
      DEALLOCATE(REAL_ALLOC)
C
      DO 20 INC1=1,10
        REAL_B2(1,INC1)=REAL_B1(INC1)*3.0
   20 CONTINUE
      DO 30 INC1=1,10
          REAL_B1(INC1)=(REAL_B1(INC1)+1.0)*2
   30 CONTINUE
      DO 40 INC1=1,10
        REAL_B2(1,INC1)=REAL_B1(INC1)+REAL_B2(1,INC1)
   40 CONTINUE
      DO 50 INC1=1,10
        REAL_B2(1,INC1)=REAL_B1(INC1)+REAL_B2(2,INC1)
   50 CONTINUE
      DO 60 INC1=1,10
        REAL_B2(1,INC1)=REAL_B1(INC1)+REAL_B2(1,INC1)
   60 CONTINUE
      DO 70 INC1=1,10
        REAL_DUMMY(1,INC1)=REAL_B1(INC1)+REAL_B2(1,INC1)
   70 CONTINUE
      DO 80 INC1=1,10
        REAL_B2(1,INC1)=REAL_DUMMY(1,INC1)
   80 CONTINUE
C
C
      DO 100 INC1=1,10
        DO 100 INC2=1,10
          IF (REAL_A2(INC1,INC2) .NE. REAL_B2(INC1,INC2)) THEN
            WRITE(6,*) '*** NG  (REAL) ***'
            WRITE(6,*) 'REAL_A2(',REAL_A2,')'
            WRITE(6,*) '"RIGHT" ',REAL_B2
            GOTO 110
          END IF
  100 CONTINUE
      WRITE (6,*) '*** OK ***'
  110 CONTINUE
      STOP
      END

      SUBROUTINE SUB(REAL_SUB1,REAL_SUB2)
      REAL REAL_SUB1(10),REAL_SUB2(10)
      REAL_SUB2=REAL_SUB1*2
      END SUBROUTINE

