      COMPLEX*8  A(100),B(100),C(100)
      COMPLEX*16 D(100),E(100),F(100)
      COMPLEX*16 G(100),H(100),O(100)
      DATA A/100*(0.0,0.0)/,B/100*(1.0,1.0)/,C/100*(2.0,2.0)/
      DATA D/100*(0.0,0.0)/,E/100*(1.0,1.0)/,F/100*(2.0,2.0)/
      DATA G/100*(0.0,0.0)/,H/100*(1.0,1.0)/,O/100*(2.0,2.0)/,N/100/
      DO 10 I=1,N
         A(I) = B(I) + C(I)
         D(I) = E(I) - F(I)
         G(I) = H(I) + O(I)
   10 CONTINUE
      WRITE(6,100) A,B,C,D,E,F,G,H,O
  100 FORMAT(10F8.2)
      STOP
      END
