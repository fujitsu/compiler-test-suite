MODULE mod1
IMPLICIT NONE

INTEGER :: i = 1
REAL,DIMENSION(3:12) :: arr = (/2.0,4.0,6.0,3.0,5.0,7.0,9.0,0.0,0.0,0.0/)

INTERFACE
SUBROUTINE sub(drr)
IMPLICIT NONE
INTEGER :: i 
REAL,DIMENSION(3:12) :: drr
END SUBROUTINE
END INTERFACE

END MODULE

PROGRAM main
USE mod1
IMPLICIT NONE

CALL sub(arr)

IF(ALL(arr(3:7) .EQ. 1.0)) THEN
  PRINT*,'pass'
ELSE
  PRINT*,101
END IF

END PROGRAM

SUBROUTINE sub(drr)
USE mod1,ONLY : i
IMPLICIT NONE
REAL,DIMENSION(1:10) :: drr
ASSOCIATE(aa => drr)
  DO WHILE(aa(i) .GT. 0.0)
    aa(i) = 1.0
    i = i + 1
  END DO
END ASSOCIATE
END SUBROUTINE
