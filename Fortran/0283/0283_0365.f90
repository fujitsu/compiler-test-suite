PROGRAM main
IMPLICIT NONE

DOUBLE PRECISION :: dist1 , dist2 
REAL(KIND = 8) :: res

dist1 = 1224895.34521361
dist2 = 1421873.67892647

res = fun(dist1,dist2)

IF(res .GT. 2000000.00000000) THEN
  PRINT*,'pass'
ELSE
  PRINT*,101
END IF

CONTAINS

FUNCTION fun(d1,d2)
IMPLICIT NONE
DOUBLE PRECISION :: d1,d2,fun
ASSOCIATE(aa => d1 + d2)
  IF(aa .GT. 2000000.00000000) THEN
    fun = aa
  ELSE
    fun = 0.0
  END IF
END ASSOCIATE
END FUNCTION

END PROGRAM
