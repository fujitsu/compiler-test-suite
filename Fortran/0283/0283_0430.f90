MODULE mod1
IMPLICIT NONE

TYPE ty
  COMPLEX(KIND = 8),pointer :: cmp1(:)
END TYPE

END MODULE

PROGRAM main
USE mod1
IMPLICIT NONE

TYPE(ty),ALLOCATABLE :: obj

ALLOCATE(obj)
ALLOCATE(obj%cmp1(5))
obj%cmp1 = (10.20,30.40)

ASSOCIATE(aa => obj%cmp1(:))
  IF(all(aa .EQ. (10.20,30.40))) PRINT*,'pass'
END ASSOCIATE

END PROGRAM
