MODULE mod1
IMPLICIT NONE

TYPE t1
  REAL(KIND = 4) :: rr
END TYPE

END MODULE

PROGRAM main
USE mod1
IMPLICIT NONE

TYPE(t1),DIMENSION(:,:),ALLOCATABLE :: obj
ALLOCATE(obj(2,3))

obj%rr = 123.900

ASSOCIATE(aa => TRANSPOSE(obj))
  IF(aa(3,2)%rr .EQ. 123.900) THEN
    PRINT*,'pass'
  ELSE
    PRINT*,101
  END IF
END ASSOCIATE

END PROGRAM
