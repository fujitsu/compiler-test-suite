MODULE mod1
IMPLICIT NONE

REAL,DIMENSION(3:12,3:12,3:12,3:12) :: arr = 0.0

INTERFACE
SUBROUTINE sub(darr)
IMPLICIT NONE
REAL,DIMENSION(3:12,3:12,3:12,3:12) :: darr
END SUBROUTINE
END INTERFACE

END MODULE

PROGRAM main
USE mod1
IMPLICIT NONE

ASSOCIATE(aa => arr)
  CALL sub(aa)
END ASSOCIATE

IF(ALL(arr(6:8:2,3:12:6,5:10:2,4:10:3) .EQ. 1.0)) THEN
  arr(:,:,:,:) = 0.0
ELSE
  arr(:,:,:,:) = 1.0
END IF

IF(ALL(arr(3:12,3:12,3:12,3:12) .EQ. 0.0)) THEN
  PRINT*,'pass'
ELSE
  PRINT*,101
END IF

END PROGRAM

SUBROUTINE sub(darr)
IMPLICIT NONE
REAL,DIMENSION(3:12,3:12,3:12,3:12) :: darr
darr(4:11:2,3:12:3,5:10:1,4:11:3) = 1.0
END SUBROUTINE
