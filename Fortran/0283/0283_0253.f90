MODULE mod1
IMPLICIT NONE
TYPE tt
  INTEGER :: i
END TYPE
END MODULE

PROGRAM main
USE mod1
IMPLICIT NONE

INTERFACE
INTEGER FUNCTION fun()
END FUNCTION
END INTERFACE

INTEGER :: arr(2:11) = 0
TYPE(tt) :: ob_1
ob_1%i = 2

ASSOCIATE(b => arr( : ob_1%i*fun(): ob_1%i))
  b(ob_1%i : fun() ) = 1
END ASSOCIATE

IF(ALL(arr(4:10:2) == 1)) THEN
  arr(4:10:2) = 0
ELSE
  arr(4:10:2) = 1
END IF

IF(ALL(arr(2:11) == 0 )) THEN
  PRINT*,'pass'
ELSE
  PRINT*,101
END IF

END PROGRAM

INTEGER FUNCTION fun()
  fun = 5
END FUNCTION
