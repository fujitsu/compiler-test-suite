MODULE mod1
IMPLICIT NONE

TYPE t1
  INTEGER(KIND = 4),DIMENSION(:),ALLOCATABLE :: num1
  INTEGER(KIND = 4),DIMENSION(:),ALLOCATABLE :: num2
END TYPE

END MODULE

PROGRAM main
USE mod1
IMPLICIT NONE

TYPE(t1) :: obj
ALLOCATE(obj%num1(5),obj%num2(5))

obj%num1 = (/100,200,300,400,500/)
obj%num2 = (/300,500,600,800,0/)

ASSOCIATE(aa => DOT_PRODUCT(obj%num1,obj%num2))
  SELECT CASE(aa)
    CASE(630000)
      PRINT*,'pass'
  END SELECT
END ASSOCIATE

END PROGRAM
