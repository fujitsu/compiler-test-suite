MODULE mod1
IMPLICIT NONE

TYPE t1
  REAL :: r1
END TYPE

TYPE,EXTENDS(t1) :: t2
  REAL :: r2
END TYPE

INTERFACE OPERATOR ( * )
  MODULE PROCEDURE multip
END INTERFACE

CONTAINS

FUNCTION multip(dd1,dd2)
IMPLICIT NONE
CLASS(t2),INTENT(IN) :: dd1 
CLASS(t2),ALLOCATABLE :: multip 
REAL,INTENT(IN) :: dd2
ALLOCATE(multip)
multip%r2 = dd1%r2 * dd2
END FUNCTION

END MODULE 

PROGRAM main
USE mod1
IMPLICIT NONE

TYPE(t2) :: obj
CLASS(t2),ALLOCATABLE :: allc
ALLOCATE(allc)
allc%r2 = 2.0
obj = fun(allc)

IF(obj%r2 .EQ. 6.0) THEN
  PRINT*,'pass'
ELSE
  PRINT*,101
END IF

CONTAINS

FUNCTION fun(dd1)
IMPLICIT NONE
CLASS(t2),ALLOCATABLE :: dd1,fun
ALLOCATE(fun)
ASSOCIATE(aa => dd1 * 3.0)
  fun%r2 = aa%r2
END ASSOCIATE
END FUNCTION

END PROGRAM
