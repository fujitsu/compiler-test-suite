MODULE mod1
IMPLICIT NONE

TYPE t1
  REAL :: r1
END TYPE

TYPE,EXTENDS(t1) :: t2
  REAL :: r2
END TYPE

END MODULE 

PROGRAM main
USE mod1
IMPLICIT NONE

CLASS(t2),DIMENSION(:),ALLOCATABLE,TARGET :: allc

ALLOCATE(allc(1:10))
allc%r2 = 2.0

ASSOCIATE(aa => fun(allc))
  SELECT TYPE(aa)
    TYPE IS(t2)
      PRINT*,'pass'
  END SELECT
END ASSOCIATE

CONTAINS

FUNCTION fun(dd1)
IMPLICIT NONE
CLASS(t1),DIMENSION(:),POINTER :: fun
CLASS(t2),DIMENSION(:),ALLOCATABLE,TARGET :: dd1
fun => dd1(1:10)
END FUNCTION

END PROGRAM
