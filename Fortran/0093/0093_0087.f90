
PROGRAM MAIN

COMPLEX::OBJW,OBJR
 INTEGER::I
OBJW=(3.44,8.44)

 OPEN (UNIT=71, FILE='fort.80', FORM='UNFORMATTED')
WRITE(UNIT=71)(OBJW,I=1,3)     
CLOSE (UNIT=71)

OPEN (UNIT=77, FILE='fort.80', FORM='UNFORMATTED')
READ(UNIT=77)(OBJR,I=1,3)     
CLOSE (UNIT=77,status='delete')


IF( OBJR%RE .ne. 3.44)print*,'101'
IF( OBJR%IM .ne. 8.44)print*,'102'



 PRINT*,'PASS'

END PROGRAM

