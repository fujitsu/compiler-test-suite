MODULE M
  TYPE T
      COMPLEX::A
END TYPE T
 



END MODULE M

PROGRAM MAIN

USE M
TYPE(T)::OBJW
REAL::R1,R2
 INTEGER::I
OBJW%A=(3.44,8.44)

 OPEN (UNIT=71, FILE='fort.81', FORM='UNFORMATTED')
     WRITE(UNIT=71)(OBJW%A%RE,I=1,3)
     WRITE(UNIT=71)(OBJW%A%IM,I=1,3)

     CLOSE (UNIT=71)


OPEN (UNIT=77, FILE='fort.81', FORM='UNFORMATTED')
     READ(UNIT=77)(R1,I=1,3)
     READ(UNIT=77)(R2,I=1,3)
     CLOSE (UNIT=77,status='delete')


IF( R1 .ne. 3.44)print*,'101'
IF( R2 .ne. 8.44)print*,'102'



 PRINT*,'PASS'

END PROGRAM

