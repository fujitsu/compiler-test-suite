
PROGRAM MAIN

COMPLEX::OBJW(2),OBJR(2)
OBJW(1)=(3.44,8.44)
OBJW(2)=(5.44,6.44)

 OPEN (UNIT=71, FILE='fort.81', FORM='FORMATTED')
     WRITE(UNIT=71,FMT='(F5.2)')OBJW%RE
     WRITE(UNIT=71,FMT='(F5.2)')OBJW%IM

     CLOSE (UNIT=71)


OPEN (UNIT=77, FILE='fort.81', FORM='FORMATTED')
     READ(UNIT=77,FMT='(F5.2)')OBJR%RE
     READ(UNIT=77,FMT='(F5.2)')OBJR%IM
     CLOSE (UNIT=77,status='delete')


IF( OBJR(1)%RE .ne. 3.44)print*,'101'
IF( OBJR(1)%IM .ne. 8.44)print*,'102'
IF( OBJR(2)%RE .ne. 5.44)print*,'103'
IF( OBJR(2)%IM .ne. 6.44)print*,'104'


 PRINT*,'PASS'

END PROGRAM

