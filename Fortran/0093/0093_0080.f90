MODULE M
  TYPE T
      COMPLEX::A
END TYPE T
 



END MODULE M

PROGRAM MAIN

USE M
TYPE(T)::OBJW1,OBJW2,OBJR1,OBJR2
OBJW1%A=(3.44,8.44)
OBJW2%A=(5.44,6.44)

 OPEN (UNIT=71, FILE='fort.81', FORM='UNFORMATTED')
     WRITE(UNIT=71)OBJW1%A%RE,OBJW2%A%RE
     WRITE(UNIT=71)OBJW1%A%IM,OBJW2%A%IM

     CLOSE (UNIT=71)


OPEN (UNIT=77, FILE='fort.81', FORM='UNFORMATTED')
     READ(UNIT=77)OBJR1%A%RE,OBJR2%A%RE
     READ(UNIT=77)OBJR1%A%IM,OBJR2%A%IM
     CLOSE (UNIT=77,status='delete')


IF( OBJR1%A%RE .ne. 3.44)print*,'101'
IF( OBJR1%A%IM .ne. 8.44)print*,'102'
IF( OBJR2%A%RE .ne. 5.44)print*,'103'
IF( OBJR2%A%IM .ne. 6.44)print*,'104'



 PRINT*,'PASS'

END PROGRAM

