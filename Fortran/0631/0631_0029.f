        G001   =B003(1,I,1,1,1,1)*C003(1,1,1,1,1,1,2)
        A003(1,I,1)=G001
        A003(2,I,1)=B003(2,I,1,1,1,1)/C003(2,1,1,1,1,1,1)
  114 CONTINUE
      DO 122 I=1,2*N1
        G005   =A005(1,I)
        A005(2,I)=G005+C005(1,I)
  122 CONTINUE
