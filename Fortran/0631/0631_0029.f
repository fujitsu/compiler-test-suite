      DATA  D005,E005,F005/60000*4.0, 60000*2.0, 30000*3.0/
      DATA  A006/60000*4.0/
      DO 101 I=1,N100-1
        G001   =A001(I)
        A001(I)=B001(I)+C001(I)
  101 CONTINUE
