PROCEDURE(DSIN),   POINTER :: PDSINP
PROCEDURE(DSINH),  POINTER :: PDSINHP
PROCEDURE(DSQRT),  POINTER :: PDSQRTP
PROCEDURE(DTAN),   POINTER :: PDTANP
PROCEDURE(DTANH),  POINTER :: PDTANHP

REAL(KIND = 8), PARAMETER :: R08 = 1.0

PDABSP   => DABS
PDACOSP  => DACOS
PDASINP  => DASIN
PDATANP  => DATAN
PDATAN2P => DATAN2
PDCOSP   => DCOS
PDCOSHP  => DCOSH
PDDIMP   => DDIM
PDEXPP   => DEXP
PDINTP   => DINT
PDLOGP   => DLOG
PDLOG10P => DLOG10
PDMODP   => DMOD
PDNINTP  => DNINT
PDPRODP  => DPROD
PDSIGNP  => DSIGN
PDSINP   => DSIN
PDSINHP  => DSINH
PDSQRTP  => DSQRT
PDTANP   => DTAN
PDTANHP  => DTANH

IF (PDABSP  (R08     ).ne.DABS  (R08     )) PRINT *, 'DABS    ERROR'
IF (PDACOSP (R08     ).ne.DACOS (R08     )) PRINT *, 'DACOS   ERROR'
IF (PDASINP (R08     ).ne.DASIN (R08     )) PRINT *, 'DASIN   ERROR'
IF (PDATANP (R08     ).ne.DATAN (R08     )) PRINT *, 'DATAN   ERROR'
IF (PDATAN2P(R08, R08).ne.DATAN2(R08, R08)) PRINT *, 'DATAN2  ERROR'
IF (PDCOSP  (R08     ).ne.DCOS  (R08     )) PRINT *, 'DCOS    ERROR'
IF (PDCOSHP (R08     ).ne.DCOSH (R08     )) PRINT *, 'DCOSH   ERROR'
IF (PDDIMP  (R08, R08).ne.DDIM  (R08, R08)) PRINT *, 'DDIM    ERROR'
IF (PDEXPP  (R08     ).ne.DEXP  (R08     )) PRINT *, 'DEXP    ERROR'
IF (PDINTP  (R08     ).ne.DINT  (R08     )) PRINT *, 'DINT    ERROR'
IF (PDLOGP  (R08     ).ne.DLOG  (R08     )) PRINT *, 'DLOG    ERROR'
IF (PDLOG10P(R08     ).ne.DLOG10(R08     )) PRINT *, 'DLOG10  ERROR'
IF (PDMODP  (R08, R08).ne.DMOD  (R08, R08)) PRINT *, 'DMOD    ERROR'
IF (PDNINTP (R08     ).ne.DNINT (R08     )) PRINT *, 'DNINT   ERROR'
IF (PDPRODP (R04, R04).ne.DPROD (R04, R04)) PRINT *, 'DPROD   ERROR'
IF (PDSIGNP (R08, R08).ne.DSIGN (R08, R08)) PRINT *, 'DSIGN   ERROR'
IF (PDSINP  (R08     ).ne.DSIN  (R08     )) PRINT *, 'DSIN    ERROR'
IF (PDSINHP (R08     ).ne.DSINH (R08     )) PRINT *, 'DSINH   ERROR'
IF (PDSQRTP (R08     ).ne.DSQRT (R08     )) PRINT *, 'DSQRT   ERROR'
IF (PDTANP  (R08     ).ne.DTAN  (R08     )) PRINT *, 'DTAN    ERROR'
IF (PDTANHP (R08     ).ne.DTANH (R08     )) PRINT *, 'DTANH   ERROR'

PRINT *,'pass'

END
