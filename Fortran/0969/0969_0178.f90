PROCEDURE(DABS),   POINTER :: PDABSP
PROCEDURE(DACOS),  POINTER :: PDACOSP
PROCEDURE(DASIN),  POINTER :: PDASINP
PROCEDURE(DATAN),  POINTER :: PDATANP
PROCEDURE(DATAN2), POINTER :: PDATAN2P
PROCEDURE(DCOS),   POINTER :: PDCOSP
PROCEDURE(DCOSH),  POINTER :: PDCOSHP
PROCEDURE(DDIM),   POINTER :: PDDIMP
