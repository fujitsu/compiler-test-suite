      PROGRAM MAIN
      INTEGER*4  NA1(10),NA2(10)
      REAL   *4  RA1(10),RA2(10),RA3(10),RA4(10)
      REAL   *8  DA1(10),DA2(10),DA3(10),DA4(10)
      COMPLEX*8  CA1(10),CA2(10)
      COMPLEX*16 CDA1(10),CDA2(10)

      DO 10 I=1,10
       NA1(I)=IEOR(IOR(I,1),-1)
       RA1(I)=FLOAT(MOD(I,2))
       DA1(I)=DFLOAT(I)
       CA1(I)=CMPLX(I*2,I/2)
 10    CDA1(I)=DCMPLX(I,-I)

      DO 20 I=1,10
       NA1(I)=ISIGN(NA1(I),INT(RA1(11-I))+NA1(I))
       NA2(I)=IABS(NINT(DA1(11-I)))-IDNINT(DPROD(RA1(MIN0(I*2,10)),1.5))
       RA2(I)=AMIN0(NA2(I),I)
       RA3(I)=DREAL(FLOAT(NA1(I))+CDA1(I))
 20    DA2(I)=DDIM(DA1(I),DBLE(AIMAG(CA1(I))))

      DO 30 I=1,10
       RA4(I)=IDIM(IOR(INT(RA1(I)),I),I**2)
       DA3(I)=DABS(DIMAG(CDA1(I)+DREAL(CDA1(I))))
       CA2(I)=CONJG(CA1(I))+MIN1(REAL(MIN(NA1(I),NA2(I),I)),99.9E+30)
       CDA2(I)=DCONJG(CDA1(I)+DCMPLX(CA2(I)))
 30    DA4(I)=ABS(DA2(I))+DPROD(RA1(I),-1*RA2(I))

      WRITE(6,*) NA1,NA2,CA1,CDA1
      WRITE(6,*) RA1,RA2,RA3,RA4
      WRITE(6,*) DA1,DA2,DA3,DA4
      END
