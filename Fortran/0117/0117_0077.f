      real*16 a(1000),b(1000)
      integer i
      ng=0
      do i = 1, 300
         diff = a(i) - sqrt(b(i))
         if (diff < -0.001 .or. 0.001 < diff) then
            ng = ng + 1
         endif
      enddo
      if (ng == 0) then
         print *, 'ok'
      else
         print *, 'ng=', ng
      endif
      end subroutine

