PROGRAM main
IMPLICIT NONE

REAL :: VOLTS,AMPS
INTEGER :: i,IN,WATTS,aa

IN=0
WATTS=1
aa=1
VOLTS=10
AMPS=20
DO 10 CONCURRENT(i=1:5)
  READ(UNIT=IN, FMT=*, ERR=10) VOLTS, AMPS
  WATTS = VOLTS * AMPS
  WAIT(aa,END =  10)
10 continue

IF(WATTS == 1) THEN
 Print *,"PASS"
ELSE
 print *,"FAIL",WATTS
END IF

END PROGRAM


