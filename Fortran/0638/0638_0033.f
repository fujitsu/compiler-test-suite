          INTEGER*4 M1(1024),M2(1024),M3(1024),M4(1024),M5 (1024),
     *              M6(1024),M7(1024),M8(1024),M9(1024),M10(1024)
          REAL*4    R1(1024),R2(1024),R3(1024),R4(1024),R5 (1024),
     *              R6(1024),R7(1024),R8(1024),R9(1024),R10(1024)
          REAL*8    D1(1024),D2(1024),D3(1024),D4(1024),D5 (1024),
     *              D6(1024),D7(1024),D8(1024),D9(1024),D10(1024)
          COMPLEX*8 C1(1024),C2(1024),C3(1024),C4(1024),C5 (1024),
     *              C6(1024),C7(1024),C8(1024),C9(1024),C10(1024)
          LOGICAL*4 L1(1024),L2(1024),L3(1024),L4(1024),L5 (1024),
     *              L6(1024),L7(1024),L8(1024),L9(1024),L10(1024)
          COMPLEX*16 CD1(1024),CD2(1024),CD3(1024),CD4(1024),CD5(1024), 
     *               CD6(1024),CD7(1024),CD8(1024),CD9(1024),CD10(1024) 

          DO 100 J=1,2
          DO  50 I=1,1024
            M1 (I) = 1
            M2(I) = 2
            M3(I) = 3
            M4(I) = 4
            M5(I) = 5
            M6(I) = 6
            M7(I) = 7
            M8(I) = 8
            M9(I) = 9
            M10(I)= 10
            R1 (I)= 1.0
            R2 (I)= 2.0
            R3 (I)= 3.0
            R4 (I)= 4.0
            R5 (I)= 5.0
            R6 (I)= 6.0
            R7 (I)= 7.0
            R8 (I)= 8.0
            R9 (I)= 9.0
            R10(I)=10.0
            D1 (I)=10.0
            D2 (I)= 9.0
            D3 (I)= 8.0
            D4 (I)= 7.0
            D5 (I)=6.0
            D6 (I)=5.0
            D7 (I)=4.0
            D8 (I)=3.0
            D9 (I)=2.0
            D10(I)=1.0
            C1(I) =(1.0,1.0)
            C2(I) =(2.0,2.0)
            C3(I) =(3.0,3.0)
            C4(I) =(4.0,4.0)
            C5(I) =(5.0,5.0)
            C6(I) =(6.0,6.0)
            C7(I) =(7.0,7.0)
            C8(I) =(8.0,8.0)
            C9(I) =(9.0,9.0)
            C10(I) =(10.0,10.0)
            L1(I)=R1(I).GT.R2(I)
            L2(I)=R2(I).LE.R3(I)
            L3(I)=R3(I).EQ.R4(I)
            L4(I)=R4(I).LT.R5(I)
            L5(I)=R5(I).GE.R6(I)
            L6(I)=R6(I).NE.R7(I)
            L7(I)=R7(I).GT.R8(I)
            L8(I)=R8(I).LT.R9(I)
            L9(I)=R9(I).GE.R1(I)
            L10(I) = I.GT.R5(I).OR.J.EQ.I
   50     CONTINUE
          DO 100 I=1,512
            CD1 (I) = (1.0,2.0)
            CD2 (I) = (1.0,2.0)
            CD3 (I) = (1.0,2.0)
            CD4 (I) = (1.0,2.0)
            CD5 (I) = (1.0,2.0)
            CD6 (I) = (1.0,2.0)
            CD7 (I) = (1.0,2.0)
            CD8 (I) = (1.0,2.0)
            CD9 (I) = (1.0,2.0)
            CD10(I) = (1.0,2.0)
            CD1 (I+512) =(2.0,1.0)
            CD2 (I+512) =(2.0,1.0)
            CD3 (I+512) =(2.0,1.0)
            CD4 (I+512) =(2.0,1.0)
            CD5 (I+512) =(2.0,1.0)
            CD6 (I+512) =(2.0,1.0)
            CD7 (I+512) =(2.0,1.0)
            CD8 (I+512) =(2.0,1.0)
            CD9 (I+512) =(2.0,1.0)
            CD10(I+512) =(2.0,1.0)
  100     CONTINUE
          DO 200 I=2,1023
            M1(I)=M2(I)+M3(I)*M4(I)+M5(I)*M6(I)+M7(I)*M9(I)-M10(I)
            R1(I)=R2(I)+R3(I)*R4(I)+R5(I)*R6(I)+R7(I)*R9(I)-R10(I)
            D1(I)=D2(I)+D3(I)*D4(I)+D5(I)*D6(I)+D7(I)*D9(I)-D10(I)
            C1(I)=C2(I)+C3(I)*C4(I)+C5(I)*C6(I)+C7(I)*C9(I)-C10(I)
            IF(L2(1)) THEN
              M1(I)=M2(I)*M3(I)+M5(I)+M6(I)*M7(I)+M8(I)+M9(I)+M10(I)+2
              R1(I)=R2(I)*R3(I)+R5(I)+R6(I)*R7(I)+R8(I)+R9(I)+R10(I)+2. 
              D1(I)=D2(I)*D3(I)+D5(I)+D6(I)*D7(I)+D8(I)+D9(I)+D10(I)+2. 
              C1(I)=C2(I)*C3(I)+C5(I)+C6(I)*C7(I)+C8(I)+C9(I)+C10(I)
     *         +(2.0,2.0)
            ELSE IF(L2(1024).AND..NOT.L2(1)) THEN
              M2(I)=M1(I)*M3(I)+M5(I)+M6(I)*M7(I)+M8(I)+M9(I)+M10(I)+2
              R2(I)=R1(I)*R3(I)+R5(I)+R6(I)*R7(I)+R8(I)+R9(I)+R10(I)+2. 
              D2(I)=D1(I)*D3(I)+D5(I)+D6(I)*D7(I)+D8(I)+D9(I)+D10(I)+2. 
              C2(I)=C1(I)*C3(I)+C5(I)+C6(I)*C7(I)+C8(I)+C9(I)+C10(I)
     *              +(2.0,2.0)
              IF(L2(I).OR.CD3(I).NE.CD4(I)) THEN
                L2(I)=.NOT.L2(I).OR.CD1(I).EQ.CD2(I)
              ENDIF
            ENDIF
  200     CONTINUE

          DO 300 I=2,1025
            WRITE(6,9999) M1(I-1),M2(I-1),M3(I-1),M4(I-1),M5(I-1)
            WRITE(6,9999) R1(I-1),R2(I-1),R3(I-1),R4(I-1),R5(I-1)
            WRITE(6,9999) D1(I-1),D2(I-1),D3(I-1),D4(I-1),D5(I-1)
            WRITE(6,9999) C1(I-1),C2(I-1),C3(I-1),C4(I-1),C5(I-1)
            WRITE(6,9999) L1(I-1),L2(I-1),L3(I-1),L4(I-1),L5(I-1)
  300     CONTINUE
          WRITE(6,9999) M6,M7,M8,M9,M10
          WRITE(6,9999) R6,R7,R8,R9,R10
          WRITE(6,9999) D6,D7,D8,D9,D10
          WRITE(6,9999) C6,C7,C8,C9,C10
          WRITE(6,9999) L6,L7,L8,L9,L10
          WRITE(6,9999) CD1,CD2,CD3,CD4,CD5,CD6,CD7,CD8,CD9,CD10
9999      format(g20.6)
          STOP
          END
