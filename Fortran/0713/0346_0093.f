      REAL*8 A1(512),B1(512),C1(512),D1(512),E1(512)
      REAL*8 F1(512),G1(512),H1(512),I1(512),J1(512)
      REAL*8 K1(512),L1(512),M1(512),N1(512),O1(512)
      REAL*4 P1(1024),Q1(1024),R1(1024),S1(1024),T1(1024)
      DATA  A1,B1,C1,D1,E1/512*1.0,512*2.0,512*3.0,512*4.0,512*5.0/
      DATA  F1,G1,H1,I1,J1/512*1.0,512*2.0,512*3.0,512*4.0,512*5.0/
      DATA  K1,L1,M1,N1,O1/512*1.0,512*2.0,512*3.0,512*4.0,512*5.0/
      DATA  P1,Q1,R1,S1,T1/1024*1.0,1024*2.0,1024*3.0,1024*4.0,1024*5.0/
      REAL*4 A2(1024,10),B2(1024,10),C2(1024,10)
      REAL*8  S4,S2,S3
      DATA A2,B2,C2/10240*2.0,10240*3.0,10240*4.0/
      DATA    S4,S2,S3/2.0,3.0,4.0/
      DATA    S/5.0/
      DO 30 J=1,10
        DO 10 I=1,512
          A1(I) = B2(I,J) * C2(I,J)
          B2(I,J) = B1(I) + C1(I)
          A2(I,J) = A1(I) / B2(I,J)
          D1(I) = B2(I,J) - E1(I)
          F1(I) = S2 * C2(I,J)
          A2(I,J) = S2 / (G1(I) + H1(I))
          I1(I) = A2(I,J) - J1(I)
          K1(I) = I1(I) - L1(I)
          M1(I) = K1(I) * S4
          B2(I,J) = M1(I) + S3
          A2(I,J) = S2 / B2(I,J)
          N1(I) = A2(I,J) - O1(I)
   10   CONTINUE
        DO 20 I=1,1024
          P1(I) = B2(I,J) * C2(I,J)
          B2(I,J) = Q1(I) + R1(I)
          S1(I) = S / A2(I,J) - T1(I)
   20   CONTINUE
   30 CONTINUE
      WRITE(6,*) A1,D1,F1,I1,K1,M1,N1,P1,S1,A2
      STOP
      END
